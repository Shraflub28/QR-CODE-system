/**
 * Get an icon by name
 * @param {string} name - Icon name
 * @param {string} className - Optional CSS class
 * @param {object} style - Optional inline styles
 * @returns {string} SVG HTML string
 */
export function getIcon(name, className = '', style = {}) {
    const icon = icons[name] || icons.info;
    const styleStr = Object.entries(style)
        .map(([key, value]) => `${key}: ${value}`)
        .join('; ');
    
    return icon
        .replace('<svg', `<svg class="${className}" style="${styleStr}"`);
}

/**
 * Create an icon element
 * @param {string} name - Icon name
 * @param {object} options - Options (className, style, size)
 * @returns {string} Icon HTML
 */
export function createIcon(name, options = {}) {
    const {
        className = '',
        style = {},
        size = 16,
        color = 'currentColor'
    } = options;
    
    const icon = icons[name] || icons.info;
    const mergedStyle = {
        width: `${size}px`,
        height: `${size}px`,
        ...style
    };
    
    return getIcon(name, className, mergedStyle)
        .replace('stroke="currentColor"', `stroke="${color}"`);
}

// Export for use in browser
if (typeof window !== 'undefined') {
    window.icons = icons;
    window.getIcon = getIcon;
    window.createIcon = createIcon;
}
